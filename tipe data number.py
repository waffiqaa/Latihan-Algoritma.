#Cara Penggunaan Tipe Data Number Python
x = 2023
y = 20.28
z = 4j
 
print(x)
print(y)
print(z)

#Untuk memeriksa tipe data dari sebuah variabel, terdapat function type() bawaan Python. Berikut contoh penggunaannya:
x = 2023
y = 20.28
z = 4j
  
print(type(x))
print(type(y))
print(type(z))

#Nilai Maksimum untuk Tipe Data Number
#Selain itu, yang sangat unik dari bahasa Python adalah nilai maksimum suatu bilangan bulat (integer) hanya dibatasi oleh kapasitas penyimpanan.Contohnya seperti berikut:
x = 999
y = -99999
z = 99999999999999999999
 
print(x)
print(y)
print(z)

#Penulisan Notasi Ilmiah (Scientific Notation)
#Untuk tipe data float, kita bisa menulisnya menggunakan notasi ilmiah atau dalam bahasa inggris disebut sebagai scientific notation:
x = 8e2
y = 8e-2
z = 1.80E5
  
print(x)
print(y)
print(z)