#Tipe Data String
pesan = "Hallo Dunia Coding"
print(pesan)
 
#Tipe Data Integer
harga = 55000
print(harga)
 
#Tipe Data Float
diskon = 99.99
print(diskon)
 
#Tipe Data Complex Number
nilai = 23j
print(nilai)
 
#Tipe Data Boolean
a = True
print(a)
 
#Tipe Data List
angka_list = ["satu","dua","tiga","satu"]
print(angka_list)
 
#Tipe Data Tuple
angka_tuple = ("satu","dua","tiga","satu")
print(angka_tuple)
 
#Tipe Data Set
angka_set = {"satu","dua","tiga","empat"}
print(angka_set)
 
#Tipe Data Dictionary
data = {"satu":1, "dua":2.13, "tiga":"a", "empat": True}
print(data)