#Operator Assignment
#Operator	Contoh	Penjelasan
#+=	a += b	a = a + b
#-=	a -= b	a = a – b
#*=	a *= b	a = a * b
#/=	a /= b	a = a / b
#%=	a %= b	a = a % b
#&=	a &= b	a = a & b
#|=	a |= b	a = a | b
#^=	a ^= b	a = a ^ b
#<<=	a <<= b	a = a << b
#>>=	a >>= b	a = a >> b

